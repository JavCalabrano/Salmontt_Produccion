    /**
     * Se refiere al boton agregar de la pestaña empleados
     * Recibe datos para construir nuevos objetos que se agregan a los objetos de cada Clase
     * @param evt
     */
    private void empleadoAddActionPerformed(ActionEvent evt) {//GEN-FIRST:event_empleadoAddActionPerformed

        // Se inician variables necesarias para crear objetos
        String nombre = empleadosNombre.getText();
        String apellidoP = empleadosApellidoP.getText();
        String apellidoM = empleadosApellidoM.getText();
        String rut = empleadosRut.getText();
        String calle = empleadoCalle.getText();
        String numero = empleadoNro.getText();
        String comuna = empleadoComuna.getText();
        String telefono = empleadoTelefono.getText();
        String email = empleadoEmail.getText();
        String cargo = empleadoCargo.getText();
        String salario = empleadoSalario.getText();
        String centroOperaciones = empleadosCentro.getText();

        // Variables para transformar String a int necesarios para crear objetos
        int fono = 0;
        int nro= 0;
        int sueldo = 0;

        // Validaciones de formato y transformaciones
        if (!Validaciones.validarRut(rut) || rut.isEmpty()){
            javax.swing.JOptionPane.showMessageDialog(this, "Formato rut incorrecto ingresar", "Error", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

        if (nombre.isEmpty() || apellidoP.isEmpty() || apellidoM.isEmpty() || calle.isEmpty() || numero.isEmpty() ||
                comuna.isEmpty() || telefono.isEmpty() || email.isEmpty() || cargo.isEmpty() || salario.isEmpty() || centroOperaciones.isEmpty()){
            javax.swing.JOptionPane.showMessageDialog(this, "Relenar todos los campos", "Error", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

        try {
            nro = Integer.parseInt(numero);
            fono = Integer.parseInt(telefono);
            sueldo = Integer.parseInt(salario);
        }catch (Exception e){
            javax.swing.JOptionPane.showMessageDialog(this, "Formato invalido para numeros", "Error", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Variable usada para extraer el Objeto al que corresponde el empleado
        UnidadOperativa centro = null;

        for (UnidadOperativa unidad : unidadOperativa) {
            if (unidad.getNombre().equals(centroOperaciones)) {
                centro = unidad;
            }
        }

        Registrable nuevoEmpleado = new Empleado(nombre,apellidoP, apellidoM, rut, new Direccion(calle, nro, comuna), fono, email, cargo, sueldo, centro);

        persona.add(nuevoEmpleado);
        cargarDatos();

        // Se limpian los cuadros de texto para volver a ingresar datos
        empleadosNombre.setText("");
        empleadosApellidoP.setText("");
        empleadosApellidoM.setText("");
        empleadosRut.setText("");
        empleadoCalle.setText("");
        empleadoNro.setText("");
        empleadoComuna.setText("");
        empleadoTelefono.setText("");
        empleadoEmail.setText("");
        empleadoCargo.setText("");
        empleadoSalario.setText("");
        empleadosCentro.setText("");


    }//GEN-LAST:event_empleadoAddActionPerformed

    /**
     * Elimina objetos de la List<Registrables>
     *     Se efectua una validación mediane el rut parra la eliminación
     * @param evt
     */
    private void empleadoRemoveActionPerformed(ActionEvent evt) {//GEN-FIRST:event_empleadoRemoveActionPerformed

        String rut = removeRutEmpleado.getText();

        //validaciones por formato y coincidencia
        if (rut.isEmpty() || !Validaciones.validarRut(rut)){
            javax.swing.JOptionPane.showMessageDialog(this, "Rut incorrecto ingresar", "Error", javax.swing.JOptionPane.WARNING_MESSAGE);
            removeRutEmpleado.setText("");
            return;
        }

        // variable destinada a guardar objeto y eliminar luego de recorrer la lista persona
        Registrable buscado = null;


        for(Registrable empleado : persona) {
            if (empleado instanceof Empleado) {
                if (((Empleado) empleado).getRutId().equals(rut)) {
                   buscado = empleado;
                }
            }
        }

        // validacion de cioncidencia
        if (buscado == null){
            javax.swing.JOptionPane.showMessageDialog(this, "Rut incorrecto ingresar", "Error", javax.swing.JOptionPane.WARNING_MESSAGE);
            removeRutEmpleado.setText("");
            return;
        }


        persona.remove(buscado);

        removeRutEmpleado.setText("");
        cargarDatos();

    }//GEN-LAST:event_empleadoRemoveActionPerformed


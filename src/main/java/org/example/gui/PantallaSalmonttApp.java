    // carga y modifica los datos consultados de las variables de Lista para personas y unidades operativas
    private void cargarDatos() {
        DefaultTableModel proveedores = (DefaultTableModel) jTable1.getModel();
        proveedores.setRowCount(0);

        DefaultTableModel clientes = (DefaultTableModel) jTable4.getModel();
        clientes.setRowCount(0);

        DefaultTableModel empleados = (DefaultTableModel) jTable3.getModel();
        empleados.setRowCount(0);

        DefaultTableModel unidadesOperativas = (DefaultTableModel) jTable2.getModel();
        empleados.setRowCount(0);

        for (UnidadOperativa dato : unidadOperativa) {

            String nombre = dato.getNombre();

            Object[] fila = new Object[5];
            fila[0] = dato.getNombre();
            fila[1] = dato.getDireccion();
            fila[2] = dato.getTipo();
            fila[3] = GestorData.empleadosCantidadCentro(nombre);
            fila[4] = GestorData.salariosCentro(nombre);

            unidadesOperativas.addRow(fila);
        }


        for (Registrable dato : persona) {


            if (dato instanceof Empleado) {
                Object[] fila = new Object[8];
                fila[0] = dato.nombreCompleto();
                fila[1] = ((Empleado) dato).getRutId();
                fila[2] = ((Empleado) dato).getDireccion();
                fila[3] = ((Empleado) dato).getTelefono();
                fila[4] = ((Empleado) dato).getEmail();;
                fila[5] = ((Empleado) dato).getCargo();
                fila[6] = ((Empleado) dato).getSalario();
                fila[7] = ((Empleado) dato).getUnidadOperativa();


                empleados.addRow(fila);

            }else if (dato instanceof Cliente && permisoAdmin) {
                Object[] fila = new Object[5];
                fila[0] = dato.nombreCompleto();
                fila[1] = ((Cliente) dato).getRutId();
                fila[2] = ((Cliente) dato).getDireccion();
                fila[3] = ((Cliente) dato).getTelefono();
                fila[4] = ((Cliente) dato).getEmail();

                clientes.addRow(fila);

            }else if(dato instanceof Proveedores) {
                Object[] fila = new Object[6];
                fila[0] = ((Proveedores) dato).getNombre();
                fila[1] = ((Proveedores) dato).getRutId();
                fila[2] = ((Proveedores) dato).getDireccion();
                fila[3] = ((Proveedores) dato).getTelefono();
                fila[4] = ((Proveedores) dato).getEmail();
                fila[5] = ((Proveedores) dato).getDescripcion();

                proveedores.addRow(fila);
            }
        }
    }



